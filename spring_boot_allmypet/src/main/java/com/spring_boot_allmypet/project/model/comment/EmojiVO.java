package com.spring_boot_allmypet.project.model.comment;


public class EmojiVO {
	public int emoNo;
	public String memId;
	public String emoDate;
	public String emoSale;
	public String emoImg;
	public String emoName;
	public String approvalStatus;
	public String approvalDate;
	
	public int getEmoNo() {
		return emoNo;
	}
	public void setEmoNo(int emoNo) {
		this.emoNo = emoNo;
	}
	public String getMemId() {
		return memId;
	}
	public void setMemId(String memId) {
		this.memId = memId;
	}
	public String getEmoDate() {
		return emoDate;
	}
	public void setEmoDate(String emoDate) {
		this.emoDate = emoDate;
	}
	public String getEmoSale() {
		return emoSale;
	}
	public void setEmoSale(String emoSale) {
		this.emoSale = emoSale;
	}
	public String getEmoImg() {
		return emoImg;
	}
	public void setEmoImg(String emoImg) {
		this.emoImg = emoImg;
	}
	public String getEmoName() {
		return emoName;
	}
	public void setEmoName(String emoName) {
		this.emoName = emoName;
	}
	public String getApprovalStatus() {
		return approvalStatus;
	}
	public void setApprovalStatus(String approvalStatus) {
		this.approvalStatus = approvalStatus;
	}
	public String getApprovalDate() {
		return approvalDate;
	}
	public void setApprovalDate(String approvalDate) {
		this.approvalDate = approvalDate;
	}
	
	
}
